void main(){

// Addition operator
num num1 = 15;
num num2 = 30;
num result = num1 + num2;
print(result);

// Subtraction Operator
num numA = 18;
num numB = 12;
num result_2 = numA - numB;
print(result_2);

// Multiply operator
num numX = 33;
num numY = 2;
num result_3 = numX * numY;
print(result_3);

// Divide operator
num numK = 45;
num numM = 2;
num result_4 = numK / numM;
print(result_4);

